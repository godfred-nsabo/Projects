// Comparison Operators always return boolean value

console.log( 10 == 10 );

console.log( 10 ==100 );

console.log( "hello" == "Hello" );

console.log( 10.5 == 10 );

console.log( undefined == undefined);

console.log( NaN == NaN );

console.log( null == null );

// Polymophism

console.log( 10 + 10 );

console.log( 10 + "10" );

console.log( null == undefined );

console.log( 10 == "10" );

console.log( "10" == 10 );

console.log( 10 === "10");

console.log( null === undefined);

// Is not Operator

console.log( null != null);

console.log( null != undefined);

console.log( 10 != 100);

console.log( 10 != "10");

console.log( 10 !== "10");

console.log( null !== undefined);


// Greater or Less

console.log( 10 > 100);

console.log( 10 >= 100);

console.log( 10 <= 100);

console.log( 10 > 1);

console.log( 10 <= 10);

// in Strings - not good to compare

console.log( "hello" > "hello");

console.log( "helloooo" > "hello");

console.log( "Hellooo" > "hello");











